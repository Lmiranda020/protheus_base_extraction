def clicar_menu_item_direto(driver, caption_texto=None, menu_id=None):
    """
    Clica em wa-menu-item que est√° diretamente no document (n√£o em Shadow DOM)
    
    Args:
        driver: WebDriver do Selenium
        caption_texto: Texto do caption (ex: "Relat√≥rios")
        menu_id: ID do elemento (ex: "COMP3072")
    
    Returns:
        bool: True se clicou, False caso contr√°rio
    """
    try:
        if menu_id:
            print(f"üîç Procurando menu por ID: {menu_id}")
            script = f"""
                let item = document.querySelector('wa-menu-item#{menu_id}');
                if (item) {{
                    item.click();
                    return true;
                }}
                return false;
            """
        elif caption_texto:
            print(f"üîç Procurando menu por caption: {caption_texto}")
            script = f"""
                let items = document.querySelectorAll('wa-menu-item');
                for (let item of items) {{
                    let caption = item.getAttribute('caption') || '';
                    if (caption.includes('{caption_texto}')) {{
                        item.click();
                        return true;
                    }}
                }}
                return false;
            """
        else:
            print("‚ùå Precisa informar caption_texto ou menu_id")
            return False
        
        resultado = driver.execute_script(script)
        
        if resultado:
            print(f"‚úÖ Menu clicado com sucesso!")
            return True
        else:
            print(f"‚ùå Menu n√£o encontrado")
            return False
            
    except Exception as e:
        print(f"‚ùå Erro ao clicar no menu: {e}")
        return False